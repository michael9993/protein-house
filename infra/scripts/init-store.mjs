async function main() {
  console.clear();
  showBanner();
  
  console.log(colorize('Welcome to the Saleor Platform Store Initializer!', 'cyan'));
  console.log('This wizard will help you set up a new store configuration.\n');

  // Parse CLI arguments
  const args = process.argv.slice(2);
  const cliConfig = {};
  
  for (let i = 0; i < args.length; i += 2) {
    const key = args[i]?.replace(/^--/, '');
    const value = args[i + 1];
    if (key && value) cliConfig[key] = value;
  }

  // Collect configuration
  const config = {
    name: cliConfig.name || await prompt('Store Name', 'My Awesome Store'),
    type: cliConfig.type || await selectOption('Store Type:', [
      { value: 'physical', description: 'Traditional retail (clothing, electronics, sports)' },
      { value: 'digital', description: 'Downloadable products (software, ebooks)' },
      { value: 'food', description: 'Food & grocery (restaurants, meal kits)' },
      { value: 'services', description: 'Service businesses (consulting, bookings)' },
      { value: 'mixed', description: 'Combination of multiple types' },
    ], 'physical'),
    tagline: cliConfig.tagline || await prompt('Store Tagline', 'Your Perfect Shopping Destination'),
    primaryColor: cliConfig.color || await prompt('Primary Brand Color (hex)', '#2563EB'),
    email: cliConfig.email || await prompt('Support Email', `support@${createSlug(cliConfig.name || 'store')}.com`),
    phone: cliConfig.phone || await prompt('Support Phone', '+1 (555) 123-4567'),
  };

  // Show summary
  console.log('\n' + colorize('━'.repeat(50), 'cyan'));
  console.log(colorize('  Store Configuration Summary', 'cyan'));
  console.log(colorize('━'.repeat(50), 'cyan'));
  console.log(`  Name:     ${colorize(config.name, 'green')}`);
  console.log(`  Type:     ${colorize(config.type, 'green')}`);
  console.log(`  Tagline:  ${colorize(config.tagline, 'green')}`);
  console.log(`  Color:    ${colorize(config.primaryColor, 'green')}`);
  console.log(`  Email:    ${colorize(config.email, 'green')}`);
  console.log(`  Phone:    ${colorize(config.phone, 'green')}`);
  console.log(colorize('━'.repeat(50), 'cyan'));

  const confirm = await prompt('\nCreate store configuration? (y/n)', 'y');
  if (!confirm.toLowerCase().startsWith('y')) {
    console.log(colorize('Operation cancelled.', 'yellow'));
    process.exit(0);
  }

  // Create files
  const slug = createSlug(config.name);
  const storesDir = path.join(__dirname, '../../storefront/src/config/stores');
  const indexPath = path.join(__dirname, '../../storefront/src/config/index.ts');

  // Create stores directory
  if (!fs.existsSync(storesDir)) {
    fs.mkdirSync(storesDir, { recursive: true });
    console.log(colorize(`\n✅ Created stores directory`, 'green'));
  }

  // Write config file
  const configPath = path.join(storesDir, `${slug}.config.ts`);
  fs.writeFileSync(configPath, generateConfig(config));
  console.log(colorize(`✅ Created store configuration: ${configPath}`, 'green'));

  // Update index
  fs.writeFileSync(indexPath, generateIndex(config));
  console.log(colorize(`✅ Updated active configuration`, 'green'));

  // Success message
  console.log('\n' + colorize('━'.repeat(50), 'green'));
  console.log(colorize('  🎉 Store Configuration Complete!', 'green'));
  console.log(colorize('━'.repeat(50), 'green'));
  console.log(colorize('\nNext Steps:', 'cyan'));
  console.log(`  1. Review your config: storefront/src/config/stores/${slug}.config.ts`);
  console.log('  2. Add your logo:      storefront/public/logo.svg');
  console.log('  3. Start the server:   cd storefront && pnpm dev');
  console.log('  4. View your store:    http://localhost:3000\n');
}

main().catch(console.error);

