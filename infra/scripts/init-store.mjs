function createSlug(name) {
  return name.toLowerCase()
    .replace(/[^a-z0-9]/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');
}

function toCamelCase(slug) {
  return slug.replace(/-([a-z])/g, (_, letter) => letter.toUpperCase());
}

async function prompt(question, defaultValue = '') {
  const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout
  });

  return new Promise((resolve) => {
    const displayQuestion = defaultValue 
      ? `${colorize(question, 'magenta')} [${defaultValue}]: `
      : `${colorize(question, 'magenta')}: `;
    
    rl.question(displayQuestion, (answer) => {
      rl.close();
      resolve(answer.trim() || defaultValue);
    });
  });
}

async function selectOption(question, options, defaultValue) {
  console.log(colorize(`\n${question}`, 'cyan'));
  options.forEach((opt, i) => {
    const marker = opt.value === defaultValue ? colorize('●', 'green') : '○';
    console.log(`  ${marker} ${opt.value.padEnd(12)} - ${opt.description}`);
  });
  
  const answer = await prompt('Your choice', defaultValue);
  const valid = options.find(o => o.value === answer);
  
  if (!valid) {
    console.log(colorize('Invalid option. Please try again.', 'red'));
    return selectOption(question, options, defaultValue);
  }
  
  return answer;
}

